    private void addTeacher() {
        if (ckeckAllTextField()) {
            Teacher teacher=new Teacher(textTeacherId.getText(), textTeacherName.getText(), textAddress.getText(), textNIC.getText(), Integer.parseInt(textTeleNo.getText()));
            try {
                boolean isAdded = TeacherController.addTeacher(teacher);;            
                if (isAdded) {
                    JOptionPane.showMessageDialog(this, "Added success!");
                } else {
                    JOptionPane.showMessageDialog(this, "Added Fail!");
                }            
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ManageTeacherDetail.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "TID is duplicate!\nPlease enter correct TID...!");
            } 
        }else{
            JOptionPane.showMessageDialog(this, "Check whether you have fill all text field");
        }
        
    }

    private void searchTeacher() {
        String name=textTeacherNameInDetail.getText();
        int index=name.length();
        
        try {
            Teacher teacher=TeacherController.searchTeacherByName(name);
            if (teacher!=null) {
                textTeacherNameInDetail.setText(teacher.getName());
                textTeacherNameInDetail.select(index, 20);
                textModifyTeacherNameInDetail.setText(teacher.getName());
                textTeacherIdInDetail.setText(teacher.getTID());
                textAddressInDetail.setText(teacher.getAddress());
                textNICInDetail.setText(teacher.getNIC());
                textTeleNoInDetail.setText(Integer.toString(teacher.getTeleNo()));
            }else{                              
                textTeacherIdInDetail.setText("");
                textAddressInDetail.setText("");
                textNICInDetail.setText("");
                textTeleNoInDetail.setText("");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ManageTeacherDetail.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ManageTeacherDetail.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

