    private void clearTextField() {
        generateTeacherId();
        textTeacherName.setText("");
        textAddress.setText("");
        textNIC.setText("");
        textTeleNo.setText("");
        
    }

    private boolean ckeckAllTextField() {
        boolean fillTeacherId=textTeacherId.getText().isEmpty();
        boolean fillTeacherName=textTeacherName.getText().isEmpty();
        boolean fillAddress=textAddress.getText().isEmpty();
        boolean fillNIC=textNIC.getText().isEmpty();
        boolean fillTeleNo=textTeleNo.getText().isEmpty();
        if (fillTeacherId || fillTeacherName || fillAddress || fillNIC || fillTeleNo) {
            return false;
        } else {
            return true;
        }
    }

    private void generateTeacherId() {
        String teacherId=null;
        try {
            teacherId=GenerateIdController.lastTableId("TID", "Teacher");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ManageTeacherDetail.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ManageTeacherDetail.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (teacherId!=null) {
            int idLength=teacherId.length();
            String idNumber=teacherId.substring(1, idLength);
            int idNumberInteger=Integer.parseInt(idNumber);
            int nextNumber=idNumberInteger+1;
            if (nextNumber<10) {
                textTeacherId.setText("t00"+nextNumber);
            }
            if (nextNumber>=10 & nextNumber<100) {
                textTeacherId.setText("t0"+nextNumber);
            }
            if (nextNumber>=100) {
                textTeacherId.setText("t"+nextNumber);
            }
        } else {
            textTeacherId.setText("t001");
        }
    }
